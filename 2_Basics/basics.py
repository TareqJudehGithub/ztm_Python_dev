"""
Data Types in Python

int
    integer  => a whole number
float
    a number with decimals
"""

# integer example
addition = 2 + 4
print(addition, type(addition))
print(10 // 2)  # >> 5  int
print(type(10 // 2))

print(2 ** 3)  # 2 to the power of 3
print(5 % 4)   # remainder is 1

print('')
# float
multiple = 2.5 * 4
print(multiple, type(multiple))
divide = 10/20
print(divide, type(divide))

print('\n')

"""
math functions
 - round() function
    Rounds to the nearest integer
 - abs() function
    Returns the absolute value of a number
"""
print(round(1.4))
print(abs(-10))



